// if statement
x = 5;
if(x > 3) {
    console.log("x is greater than 3");
}

// if else statement
x = 5;
if(x > 3) {
    console.log("x is greater than 3");
}
else {
    console.log("x is less than or equal to 3");
}

// if else if statement
x = 5;
if(x > 3) {
    console.log("x is greater than 3");
}
else if(x < 3) {
    console.log("x is less than 3");
}
else {
    console.log("x is equal to 3");
}

// switch statement
x = 5;
switch(x) {
    case 1:
        console.log("x is 1");
        break;
    case 2:
        console.log("x is 2");
        break;
    case 3:
        console.log("x is 3");
        break;
    default:
        console.log("x is not 1, 2, or 3");
        break;
}

